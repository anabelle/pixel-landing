type TranslationValue = string | { [key: string]: TranslationValue };

export type Locale = keyof typeof translations;
export type TranslationKey = string;

export function getTranslations(locale: Locale) {
  return translations[locale];
}

export function t(locale: Locale | undefined, key: string): string {
  const safeLocale = locale && translations[locale] ? locale : 'en';
  const keys = key.split('.');
  let value: TranslationValue | undefined = translations[safeLocale];

  for (const k of keys) {
    if (typeof value === 'object' && value !== null) {
      value = (value as { [key: string]: TranslationValue })[k];
    } else {
      value = undefined;
      break;
    }
  }

  return typeof value === 'string' ? value : key;
}